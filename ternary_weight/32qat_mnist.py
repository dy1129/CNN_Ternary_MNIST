def read_hex12_signed(path, wh=None):
    """ 12-bit 2's complement HEX 파일을 읽어 signed int32 numpy 배열로 반환 """
    vals=[]
    with open(path) as f:
        for line in f:
            s=line.strip()
            if not s: continue
            v = int(s, 16) & 0xFFF
            if v >= 0x800: v -= 0x1000 # 12-bit 2's complement
            vals.append(v)
    a = np.array(vals, dtype=np.int32)
    if wh is not None:
        H,W = wh
        assert a.size == H*W, f"Expected {H}x{W}, got {a.size}"
        a = a.reshape(H,W)
    return a

def read_hex8_signed(path, n=None):
    """ 8-bit 2's complement HEX 파일을 읽어 signed int32 numpy 배열로 반환 """
    vals=[]
    with open(path) as f:
        for line in f:
            s=line.strip()
            if not s: continue
            v = int(s, 16) & 0xFF
            if v >= 128: v -= 256
            vals.append(v)
    return np.array(vals if n is None else vals[:n], dtype=np.int32)

def write_hex12_2s(path, arr_hw):
    with open(path, "w") as f:
        for v in arr_hw.reshape(-1):
            v = int(np.clip(v, -2048, 2047))
            if v < 0: v = (v + (1<<12)) & 0xFFF
            f.write(f"{v:03X}\n")

def im2col5x5_stride1(img):
    # img: HxW -> ( (H-4)*(W-4), 25)
    H,W = img.shape
    out=[]
    OH = H - 5 + 1
    OW = W - 5 + 1
    for y in range(OH):
        for x in range(OW):
            patch = img[y:y+5, x:x+5].reshape(-1).astype(np.int32)
            out.append(patch)
    return np.stack(out, axis=0) # (OH*OW, 25)

def sat12_np(a):
    """ NumPy 배열에 12비트 포화 (SAT_MIN/MAX) 적용 """
    return np.clip(a, SAT_MIN, SAT_MAX).astype(np.int32)

def maxpool_2x2_relu(img_map_in_hw):
    """ RTL의 maxpool_relu 모듈  (2x2 풀링 + ReLU)을 정확히 모델링 """
    H_in, W_in = img_map_in_hw.shape
    assert H_in % 2 == 0 and W_in % 2 == 0, "입력 크기는 짝수여야 합니다."
    H_out, W_out = H_in // 2, W_in // 2
    img_map_out = np.zeros((H_out, W_out), dtype=np.int32)

    for y in range(H_out):
        for x in range(W_out):
            y_in, x_in = y*2, x*2
            # 2x2 윈도우에서 max 찾기
            p1 = img_map_in_hw[y_in,     x_in]
            p2 = img_map_in_hw[y_in,     x_in + 1]
            p3 = img_map_in_hw[y_in + 1, x_in]
            p4 = img_map_in_hw[y_in + 1, x_in + 1]
            max_val = np.max([p1, p2, p3, p4])

            # ReLU 적용
            relu_val = max_val if max_val > 0 else 0
            img_map_out[y, x] = relu_val

    return img_map_out

def q2p6_round_shift(x_int):
    """ RTL의 Q2.6 라운딩 시프트 (alpha, bias 공통)
        (x + 32) >> 6 (ROUND_TERNARY = 32)
    """
    return (x_int + ROUND_TERNARY) >> 6

