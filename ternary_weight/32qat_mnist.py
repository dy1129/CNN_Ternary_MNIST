    W_fc_flat = read_hex8_signed(FC_W_P)
    W_fc = W_fc_flat.reshape(10, 48)     # (OC=10, IC=48)

    biases_fc_q26 = read_hex8_signed(FC_B_P, n=10)
    alphas_fc_q26 = read_hex8_signed(FC_S_P, n=10)

    biases_fc_int = q2p6_round_shift(biases_fc_q26.astype(np.int64))
    alphas_fc_int = alphas_fc_q26.astype(np.int64)
    X_fc = x_flat_fc_input.astype(np.int64) # (48,)

    # ----------------------------------------------------
    ## 🚀 FC 가중치 행렬 (10x48) 출력 추가
    # ----------------------------------------------------
    print("\n" + "="*70)
    print(" 🚀 FC Weight Matrix (W_fc: 10x48, Ternary: -1/0/+1)")
    print("   [OC=Row (0-9), IC=Column (0-47)]")
    print("="*70)

    # NumPy 설정: 모든 요소를 출력하고, 값 앞에 부호를 표시하여 -1/0/+1 구분이 쉽도록 합니다.
    # W_fc는 10x48 행렬입니다.
    # np.set_printoptions(linewidth=150, threshold=480, formatter={'all': lambda x: f'{x:+2d}'})
    # print(W_fc)
    # np.set_printoptions(formatter=None) # 설정 초기화

    # 행 단위로 출력하여 가독성 높이기
    for oc in range(W_fc.shape[0]):
        # +1, 0, -1을 +1,  0, -1 형태로 출력 (2자리 고정)
        row_str = " ".join(f"{v:+2d}" for v in W_fc[oc].tolist())
        print(f"OC {oc:02d}: {row_str}")

    print("="*70 + "\n")
    # ----------------------------------------------------

    # 2. FC 연산 (Dot Product)
    psum_raw_fc = np.zeros(10, dtype=np.int64)

    for oc in range(10):
        W_oc = W_fc[oc]

        # Ternary MAC 로직 (기존 코드 유지)
        ternary_mul_fc = np.where(W_oc == 1, X_fc, np.where(W_oc == -1, -X_fc, 0))
        psum_raw_oc = np.sum(ternary_mul_fc)
        psum_raw_fc[oc] = psum_raw_oc

    # 3. α 스케일링 + Bias 덧셈 + Saturation
    ACC_MIN_FC = -(1 << (FC_ACCUM_BITS - 1))
    ACC_MAX_FC = (1 << (FC_ACCUM_BITS - 1)) - 1
    psum_fc_sat = np.clip(psum_raw_fc, ACC_MIN_FC, ACC_MAX_FC)

    m_a_fc = psum_fc_sat * alphas_fc_int
    scaled_fc = q2p6_round_shift(m_a_fc)
    summed_fc = scaled_fc + biases_fc_int

    # 최종 12-bit 포화 (Logits)
    logits_fc = sat12_np(summed_fc)

    print("\n" + "="*40)
    print("   FC Layer Output (Logits)")
    print("="*40)
    print("Raw Psum (before scaling/saturation):")
    print(psum_raw_fc)
    print("\nFinal Logits (12-bit Integer):")
    print(logits_fc)
    print(f"\nPredicted Class (Max Index): {np.argmax(logits_fc)}")

    # HEX 출력
    print_hex12_2s_console(logits_fc)

    # 파일 저장
    LOGITS_P = OUT_DIR/"fc_logits_10x1_12b.hex"
    write_hex12_2s(LOGITS_P, logits_fc)
    print(f"\n[OK] FC Logits saved to: {LOGITS_P}")

    return logits_fc

