from pathlib import Path

# *****************************************************************
# ⚠️ 주의: 입력 파일 경로를 실제 환경에 맞게 정확히 지정해야 합니다.
# 두 번째 정의된 경로를 사용합니다.
IN = Path("runs/mnist_twn_qat_v4_precision/fc_weight.txt")
# *****************************************************************

OUT = Path("fc_weight_block_reversed.txt")

LINES_PER_BLOCK = 48
TOTAL_BLOCKS = 10
TOTAL_LINES = TOTAL_BLOCKS * LINES_PER_BLOCK # 480줄

def reverse_blocks():
    """
    입력 파일을 읽어 48줄 블록 10개의 순서를 역순으로 뒤집고,
    그 결과를 새 파일에 저장합니다.
    """
    try:
        # 파일 읽기 (빈 줄 제거)
        lines = [ln.strip() for ln in IN.read_text(encoding="utf-8").splitlines() if ln.strip()]
    except FileNotFoundError:
        # 이 오류는 사용자의 파일 시스템 문제이므로, 경로 확인이 필요합니다.
        return f"🚨 오류: 입력 파일 '{IN}'을 찾을 수 없습니다. 파일 경로가 정확한지 확인해 주세요."
    except Exception as e:
        return f"⚠️ 오류: 파일을 읽는 중 문제가 발생했습니다: {e}"

    # 데이터 길이 확인 (480줄 확인)
    if len(lines) != TOTAL_LINES:
        return f"❌ 오류: 파일의 줄 수가 {TOTAL_LINES}줄이어야 하는데, 현재 {len(lines)}줄입니다."

    # 1. 48줄씩 10개의 블록으로 분리
    blocks = []
    for i in range(0, len(lines), LINES_PER_BLOCK):
        blocks.append(lines[i:i + LINES_PER_BLOCK])

    # 2. 블록 자체의 순서를 역순으로 뒤집기 (요청하신 작업: [B10, B9, ..., B1])
    reversed_blocks = blocks[::-1]

    # 3. 뒤집힌 블록을 다시 하나의 리스트로 결합
    out = []
    for block in reversed_blocks:
        out.extend(block)

    # 4. 파일 쓰기
    try:
        # 마지막에 빈 줄을 추가하여 파일 쓰기 완료
        OUT.write_text("\n".join(out) + "\n", encoding="utf-8")
        return f"✅ 성공: 파일에 성공적으로 저장되었습니다: {OUT} (총 {len(out)} lines)"
    except Exception as e:
        return f"⚠️ 오류: 파일을 쓰는 중 문제가 발생했습니다: {e}"

# --- 이 함수를 파이썬 환경에서 실행하세요 ---
