def rtl_fsm_maxpool_relu(stream_in_96):
    """
    [FIX] maxpool_relu.v FSM을 사이클 단위로 1:1 에뮬레이션
    "8-valid, 4-invalid" (총 12개) 스트림 8줄을 입력받아 4x4 출력 생성
    """
    HALF_WIDTH = 4 # u_pool2의 HALF_WIDTH=4
    buffer1 = np.zeros(HALF_WIDTH, dtype=np.int32)

    pcount = 0
    state = 0 # 0 = row 0, 1 = row 1
    flag = 0  # 0 = col 0, 1 = col 1

    # "8-valid, 4-invalid" 96-cycle valid 스트림 생성
    valid_stream = []
    for i in range(8): # 8줄
        valid_stream.extend([1] * 8) # 8-valid
        valid_stream.extend([0] * 4) # 4-invalid

    outputs = []

    # 96 사이클 전체를 순회
    for i in range(96):
        valid_in = valid_stream[i]
        conv_out_1 = stream_in_96[i]

        # RTL의 always @(posedge clk)에 해당

        if valid_in == 1:
            # Verilog의 non-blocking 할당(<=)을 위해,
            # 이번 사이클 계산에는 *이전* 사이클의 FSM 상태값을 사용합니다.
            current_flag = flag
            current_pcount = pcount
            current_state = state

            # --- 1. 다음 상태(Next State) 계산 ---
            # flag <= ~flag;
            next_flag = 1 - current_flag

            next_pcount = current_pcount
            next_state = current_state

            # if(flag == 1)
            if current_flag == 1: # 2번째 또는 4번째 입력이 들어온 사이클
                # pcount <= pcount + 1;
                next_pcount = current_pcount + 1
                # if(pcount == HALF_WIDTH - 1)
                if next_pcount == HALF_WIDTH:
                    next_pcount = 0
                    # state <= ~state;
                    next_state = 1 - current_state

            # --- 2. 현재 상태(Current State)로 연산 수행 ---

            # if(state == 0)
            if current_state == 0: # First line
                # if(flag == 0)
                if current_flag == 0: # 1st input
                    buffer1[current_pcount] = conv_out_1
                else: # 2nd input -> comparison
                    if buffer1[current_pcount] < conv_out_1:
                        buffer1[current_pcount] = conv_out_1

            # else (state == 1)
            else: # Second line
                # if(flag == 0)
                if current_flag == 0: # 3rd input
                    if buffer1[current_pcount] < conv_out_1:
                        buffer1[current_pcount] = conv_out_1
                else: # 4th input -> comparison + relu
                    # valid_out_relu <= 1;

                    # Max(buffer, conv_out)
                    temp_max = conv_out_1 if buffer1[current_pcount] < conv_out_1 else buffer1[current_pcount]

                    # ReLU (signed-safe: 0보다 작으면 0)
                    relu_out = temp_max if temp_max > 0 else 0
                    outputs.append(relu_out)

            # --- 3. FSM 상태 업데이트 (다음 클럭을 위해) ---
            flag = next_flag
            pcount = next_pcount
            state = next_state

        # if (valid_in == 0):
        #   FSM이 "얼어붙습니다". flag, pcount, state가 변하지 않습니다.
        #   NumPy 루프는 다음 사이클(i+1)로 넘어갑니다.

    return np.array(outputs, dtype=np.int32).reshape(4, 4)

