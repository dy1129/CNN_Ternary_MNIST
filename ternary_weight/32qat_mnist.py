# ===== Helpers (fixed-point / IO) =====
TERNARY_T = 0.7
SHIFT_CONV1 = 15
SHIFT_TERNARY = 6
ROUND_CONV1 = (1 << (SHIFT_CONV1 - 1))
ROUND_TERNARY = (1 << (SHIFT_TERNARY - 1))

# [FIX] RTL 내부 정밀도 정의
CONV2_ACCUM_BITS = 22       # conv2_calc.v: psum_raw (SUM_W) = 22 bits
FC_ACCUM_BITS    = 28       # fc.v: s1 [PROD_W+5:0] -> [27:0] = 28 bits

# 최종 12비트 포화
SAT_MIN = -2048
SAT_MAX = 2047

def q1_15_from_scale(s: float) -> int:
    v = int(np.round(np.clip(float(s), -1.0, (1.0 - 2**-15)) * (1 << 15)))
    return v & 0xFFFF

def q2_6_from_float(x: float) -> int:
    v = int(np.round(np.clip(float(x), -2.0, (2.0 - 2**-6)) * (1 << 6)))
    return v & 0xFF

def int8_twos(x: int) -> int:
    xi = int(x)
    return (xi + (1 << 8)) & 0xFF if xi < 0 else xi & 0xFF

def write_hex_lines(path, values, width_bits):
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    fmt = "{:02X}\n" if width_bits == 8 else "{:04X}\n"
    mask = (1 << width_bits) - 1
    with open(path, "w") as f:
        for v in values:
            f.write(fmt.format(int(v) & mask))

# ===== QAT / STE =====
class ArithmeticRightShiftSTE(torch.autograd.Function):
    """ RTL의 (x + R) >>> S (Round-to-nearest-up) 에뮬레이션 + STE """
    @staticmethod
    def forward(ctx, x, shift_bits, round_const):
        divisor = (1 << shift_bits)
        return torch.floor((x + round_const) / divisor)
    @staticmethod
    def backward(ctx, g):
        return g, None, None

class FakeQuantQ2_6STE(torch.autograd.Function):
    @staticmethod
    def forward(ctx, x):
        v = torch.round(torch.clamp(x, -2.0, (2.0 - 2**-6)) * (1 << SHIFT_TERNARY))
        return v
    @staticmethod
    def backward(ctx, g):
        return g

class FakeQuantQ1_15STE(torch.autograd.Function):
    @staticmethod
    def forward(ctx, x_float):
        v = torch.round(torch.clamp(x_float, -1.0, (1.0 - 2**-15)) * (1 << SHIFT_CONV1))
        return v
    @staticmethod
    def backward(ctx, g):
        return g

class ClampSTE(torch.autograd.Function):
    @staticmethod
    def forward(ctx, x, min_val, max_val):
        return torch.clamp(x, min_val, max_val)
    @staticmethod
    def backward(ctx, g):
        return g, None, None

# ===== Ternary (QAT with STE) =====
class TernaryQuantFn(torch.autograd.Function):
    @staticmethod
    def forward(ctx, w, delta):
        pos = (w > delta).to(w.dtype)
        neg = (w < -delta).to(w.dtype)
        return pos - neg  # {-1,0,+1}
    @staticmethod
    def backward(ctx, g):
        return g, None

def ternary_with_alpha(w, t=TERNARY_T, per_channel_dim=0):
    dims = list(range(w.dim()))
    red = dims[:]; red.pop(per_channel_dim)
    mean_abs = w.abs().mean(dim=red, keepdim=True)
    delta = t * mean_abs
    tern = TernaryQuantFn.apply(w, delta)
    mask = tern.ne(0).to(w.dtype)
    denom = mask.sum(dim=red, keepdim=True).clamp_min(1.0)
    alpha = (w.abs()*mask).sum(dim=red, keepdim=True)/denom
    return tern, alpha

class TernaryConv2d(nn.Module):
    # conv2_calc.v (SUM_W = 22 bits)
    def __init__(self, in_ch, out_ch, k=5, stride=1, padding=0, t=TERNARY_T, bias=True):
        super().__init__()
        self.weight_fp = nn.Parameter(torch.empty(out_ch, in_ch, k, k))
        self.bias = nn.Parameter(torch.zeros(out_ch)) if bias else None
        self.stride, self.padding, self.t = stride, padding, t
        nn.init.kaiming_normal_(self.weight_fp, nonlinearity='relu')
        self.ACC_MAX = (1 << (CONV2_ACCUM_BITS - 1)) - 1
        self.ACC_MIN = -(1 << (CONV2_ACCUM_BITS - 1))

    def forward(self, x):
        # x shape: (N, C_in=3, H_in, W_in)
        tern_w, alpha_f = ternary_with_alpha(self.weight_fp, t=self.t, per_channel_dim=0)
        # tern_w shape: (C_out=3, C_in=3, 5, 5)

        # --- [FIX] Emulate RTL's 3 separate Adder Trees (AT1, AT2, AT3) ---
        psum_f32 = 0.0
        for ic in range(x.size(1)): # C_in=3
            # 1. 1-channel convolution (emulates one Adder Tree)
            # x[:, ic:ic+1] -> (N, 1, H, W)
            # tern_w[:, ic:ic+1] -> (C_out, 1, 5, 5)
            # groups=C_out (Depthwise) ensures C_out filters apply to C_in=1
            # We must simulate C_out convolutions, each on 1 input channel

            # This is complex. Let's simplify:
            # F.conv2d(x_in, w_in) does sum(x_ic * w_oc_ic)
            # We need sum( clamp(x1*w1, 22b) + clamp(x2*w2, 22b) + clamp(x3*w3, 22b) )

            # F.conv2d(x, tern_w, ...) already calculates sum(x_ic * w_oc_ic)
            # What RTL does is sum_ic( AdderTree(x_ic * w_oc_ic) )
            # The AdderTree(x*w) clamps the sum of 25 products to 22 bits.

            # Let's emulate sum1, sum2, sum3
            # x_ic: (N, 1, H_in, W_in)
            x_ic = x[:, ic:ic+1, ...]

            # w_oc_ic: (C_out, 1, 5, 5)
            w_oc_ic = tern_w[:, ic:ic+1, ...].to(x.dtype)

            # sum_ic = F.conv2d(x_ic, w_oc_ic)
            # This isn't right. F.conv2d(N,1,H,W) with (C_out,1,K,K) is what we need.

            sum_ic_f32 = F.conv2d(x_ic, w_oc_ic, bias=None, stride=self.stride, padding=self.padding)

            # 2. [FIX] Clamp each input channel's sum to 22-bits (emulating sum1, sum2, sum3)
            sum_ic_clamped = ClampSTE.apply(sum_ic_f32, self.ACC_MIN, self.ACC_MAX)

            # 3. Add to total psum_raw
            psum_f32 = psum_f32 + sum_ic_clamped

        # y_acc is psum_raw. This final sum can exceed 22 bits, which is fine.
        y_acc = psum_f32
        # --- End of RTL Emulation ---

        # 3. RTL QAT (Scaling, Bias, Shift)
        alpha_q = FakeQuantQ2_6STE.apply(alpha_f).view(1, -1, 1, 1)
        bias_q  = FakeQuantQ2_6STE.apply(self.bias).view(1, -1, 1, 1)

        # 4. RTL 시프트 연산 (STE)
        s = ArithmeticRightShiftSTE.apply(y_acc * alpha_q, SHIFT_TERNARY, ROUND_TERNARY)
        b = ArithmeticRightShiftSTE.apply(bias_q, SHIFT_TERNARY, ROUND_TERNARY)

        # 5. 덧셈 및 12-bit 포화
        y = torch.clamp(s + b, SAT_MIN, SAT_MAX)
        return y

    @torch.no_grad()
    def export(self):
        tern, alpha = ternary_with_alpha(self.weight_fp, t=self.t, per_channel_dim=0)
        a = alpha[:,0,0,0]
        return tern.cpu().to(torch.int8), a.cpu().to(torch.float32)

class TernaryLinear(nn.Module):
    # fc.v: s1 (PROD_W+5:0) = 28 bits
    def __init__(self, in_f, out_f, t=TERNARY_T, bias=True):
        super().__init__()
        self.weight_fp = nn.Parameter(torch.empty(out_f, in_f))
        self.bias = nn.Parameter(torch.zeros(out_f)) if bias else None
        self.t = t
        nn.init.kaiming_uniform_(self.weight_fp, a=np.sqrt(5))
        self.ACC_MAX = (1 << (FC_ACCUM_BITS - 1)) - 1
        self.ACC_MIN = -(1 << (FC_ACCUM_BITS - 1))

    def forward(self, x):
        tern_w, alpha_f = ternary_with_alpha(self.weight_fp, t=self.t, per_channel_dim=0)
        y_acc_f32 = F.linear(x, tern_w.to(x.dtype), bias=None)
        y_acc = ClampSTE.apply(y_acc_f32, self.ACC_MIN, self.ACC_MAX)
        alpha_q = FakeQuantQ2_6STE.apply(alpha_f[:,0]).view(1, -1)
        bias_q  = FakeQuantQ2_6STE.apply(self.bias).view(1, -1)
        s = ArithmeticRightShiftSTE.apply(y_acc * alpha_q, SHIFT_TERNARY, ROUND_TERNARY)
        b = ArithmeticRightShiftSTE.apply(bias_q, SHIFT_TERNARY, ROUND_TERNARY)
        y = torch.clamp(s + b, SAT_MIN, SAT_MAX)
        return y

    @torch.no_grad()
    def export(self):
        tern, alpha = ternary_with_alpha(self.weight_fp, t=self.t, per_channel_dim=0)
        return tern.cpu().to(torch.int8), alpha[:,0].cpu().to(torch.float32)

# ===== conv1: INT8 QAT wrapper (flip 지원) =====
class Int8PerOCConv2d_QAT(nn.Module):
    def __init__(self, in_ch, out_ch, k=5, stride=1, padding=0, bias=True, flip=True):
        super().__init__()
        self.weight_fp = nn.Parameter(torch.empty(out_ch, in_ch, k, k))
        self.bias = nn.Parameter(torch.zeros(out_ch)) if bias else None
        self.stride, self.padding = stride, padding
        self.flip = flip
        nn.init.kaiming_normal_(self.weight_fp, nonlinearity='relu')

    def _maybe_flip(self, w):
        if not self.flip: return w
        return torch.flip(w, dims=[-1, -2])

    def forward(self, x):
        w_flipped = self._maybe_flip(self.weight_fp)
        qW_list, S_q1_15_list = [], []
        for oc in range(w_flipped.size(0)):
            w_oc = w_flipped[oc]
            max_abs_val = w_oc.abs().max().clamp(min=1e-12)
            inv_s_tensor = max_abs_val / 127.0   # RTL dequant scale
            S_q1_15_oc = FakeQuantQ1_15STE.apply(inv_s_tensor)
            S_q1_15_list.append(S_q1_15_oc)
            quant_scale_s = 127.0 / max_abs_val
            qW_oc = torch.clamp(torch.round(w_oc * quant_scale_s), -128, 127)
            qW_oc_ste = (qW_oc - w_oc).detach() + w_oc
            qW_list.append(qW_oc_ste)
        qW_ste = torch.stack(qW_list, dim=0)
        S_q1_15_ste = torch.stack(S_q1_15_list, dim=0).view(1, -1, 1, 1)

        # bias: int8 (스케일 없이 더함)
        bias_int8 = torch.round(torch.clamp(self.bias, -128, 127))
        bias_q_ste = (bias_int8 - self.bias).detach() + self.bias
        bias_q_ste = bias_q_ste.view(1, -1, 1, 1)

        y_acc = F.conv2d(x, qW_ste.to(x.dtype), bias=None, stride=self.stride, padding=self.padding)
        s = ArithmeticRightShiftSTE.apply(y_acc * S_q1_15_ste, SHIFT_CONV1, ROUND_CONV1)
        y = torch.clamp(s + bias_q_ste, SAT_MIN, SAT_MAX)
        return y

    @torch.no_grad()
    def export(self):
        w = self._maybe_flip(self.weight_fp.detach().cpu())
        oc, ic, k, _ = w.shape
        qW_export = torch.empty_like(w, dtype=torch.int8)
        scales_q1_15_u16_export = []
        for o in range(oc):
            m = w[o].abs().max().item()
            s = 127.0 / (m + 1e-12)
            q = torch.clamp(torch.round(w[o] * s), -128, 127).to(torch.int8)
            inv_s = 1.0 / s
            qW_export[o] = q
            scales_q1_15_u16_export.append(q1_15_from_scale(inv_s))
        b_int8_export = torch.clamp(torch.round(self.bias.detach().cpu()), -128, 127).to(torch.int32)
        return qW_export, scales_q1_15_u16_export, b_int8_export

# ===== Net (28→24→pool→12→8→pool→4; 3*4*4=48 → FC10) =====
class Net(nn.Module):
    def __init__(self, t=TERNARY_T):
        super().__init__()
        self.conv1 = Int8PerOCConv2d_QAT(1, 3, 5, bias=True, flip=True)
        self.conv2 = TernaryConv2d(3, 3, 5, t=t, bias=True)
        self.pool  = nn.MaxPool2d(2)
        self.fc    = TernaryLinear(48, 10, t=t, bias=True)
    def forward(self, x):
        x = x * 255.0
        x = self.pool(F.relu(self.conv1(x)))
        x = self.pool(F.relu(self.conv2(x)))
        x = x.view(x.size(0), -1)
        return self.fc(x)

# ===== Train / Eval =====
def get_loaders(batch, workers, data_dir):
    tfm = transforms.Compose([transforms.ToTensor()])
    tr = datasets.MNIST(data_dir, train=True,  download=True, transform=tfm)
    te = datasets.MNIST(data_dir, train=False, download=True, transform=tfm)
    return (DataLoader(tr, batch_size=batch, shuffle=True,  num_workers=workers, pin_memory=True),
            DataLoader(te, batch_size=512,  shuffle=False, num_workers=workers, pin_memory=True))

def train_one_epoch(net, loader, opt, device):
    net.train(); ce = nn.CrossEntropyLoss()
    tot=0; corr=0; loss_sum=0.0
    for x,y in loader:
        x,y = x.to(device), y.to(device)
        opt.zero_grad(set_to_none=True)
        logit = net(x)
        loss = ce(logit,y); loss.backward(); opt.step()
        loss_sum += loss.item()*x.size(0)
        corr += (logit.argmax(1)==y).sum().item(); tot += x.size(0)
    return loss_sum/tot, corr/tot

@torch.no_grad()
def evaluate(net, loader, device):
    net.eval(); ce=nn.CrossEntropyLoss()
    tot=0; corr=0; loss_sum=0.0
    for x,y in loader:
        x,y = x.to(device), y.to(device)
        logit = net(x); loss = ce(logit,y)
        loss_sum += loss.item()*x.size(0)
        corr += (logit.argmax(1)==y).sum().item(); tot += x.size(0)
    return loss_sum/tot, corr/tot

