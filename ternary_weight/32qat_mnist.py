@torch.no_grad()
def export_txt_from_model(model: Net, outdir="weights_txt", fc_order="CHW"):
    out = Path(outdir); out.mkdir(parents=True, exist_ok=True)

    # conv1: INT8 per-OC + inv_scale(Q1.15)
    qW1, S1_u16, b1_int8 = model.conv1.export()
    qW1 = qW1.squeeze(1).numpy()
    names_c1 = ["conv1_weight_1.txt","conv1_weight_2.txt","conv1_weight_3.txt"]
    for oc, fname in enumerate(names_c1):
        vals = qW1[oc].reshape(-1).tolist()
        write_hex_lines(out/fname, [int8_twos(int(v)) for v in vals], 8)
    write_hex_lines(out/"conv1_bias.txt",  [int8_twos(int(v)) for v in b1_int8.tolist()], 8)
    write_hex_lines(out/"conv1_scale.txt", [int(x) for x in S1_u16], 16)

    # conv2 (ternary + alpha/bias Q2.6)
    t2_w, t2_a = model.conv2.export()
    w2 = t2_w.numpy(); a2 = t2_a.numpy()
    names = [
        ["conv2_weight_11.txt","conv2_weight_12.txt","conv2_weight_13.txt"],
        ["conv2_weight_21.txt","conv2_weight_22.txt","conv2_weight_23.txt"],
        ["conv2_weight_31.txt","conv2_weight_32.txt","conv2_weight_33.txt"],
    ]
    for oc in range(3):
        for ic in range(3):
            write_hex_lines(out/names[oc][ic],
                            [int8_twos(int(v)) for v in w2[oc,ic].reshape(-1).tolist()], 8)
    b2 = model.conv2.bias.detach().cpu().numpy()
    write_hex_lines(out/"conv2_alpha.txt", [q2_6_from_float(float(x)) for x in a2.tolist()], 8)
    write_hex_lines(out/"conv2_bias.txt",  [q2_6_from_float(float(x)) for x in b2.tolist()], 8)

    # FC (ternary + alpha/bias Q2.6)
    t_fc, a_fc = model.fc.export()
    wfc = t_fc.numpy(); afc = a_fc.numpy()

    # RTL FSM: CHW 플래튼 순서
    if fc_order.upper() == "CHW":
        seq=[]
        for k in range(10):
            seq.extend(wfc[k].reshape(-1).tolist())
    else:
        raise ValueError(f"FC order {fc_order} not supported by RTL FSM. Use CHW.")

    write_hex_lines(out/"fc_weight.txt", [int8_twos(int(v)) for v in seq], 8)
    bfc = model.fc.bias.detach().cpu().numpy()
    write_hex_lines(out/"fc_scale.txt", [q2_6_from_float(float(x)) for x in afc.tolist()], 8)
    write_hex_lines(out/"fc_bias.txt",  [q2_6_from_float(float(x)) for x in bfc.tolist()], 8)

    (out/"manifest.json").write_text(json.dumps({
        "note": "All txt files are $readmemh-compatible (one hex per line).",
        "conv1_scale_q1_15_u16": [int(x) for x in S1_u16]
    }, indent=2), encoding="utf-8")
    print(f"[OK] wrote txt weights under: {out}")

