@torch.no_grad()
def reeval_fc_from_txt(save_dir="runs/mnist_twn_qat_v4_precision/weights_txt",
                       fc_input_path="runs/debug_dump/fc_input_chw.txt"):
    import numpy as np
    sd = Path(save_dir)
    w_txt = sd/"fc_weight.txt"
    s_txt = sd/"fc_scale.txt"
    b_txt = sd/"fc_bias.txt"

    feat = np.loadtxt(fc_input_path, dtype=np.int32)        # 48
    whex = [int(line.strip(), 16) for line in w_txt.read_text().splitlines()]  # 10*48
    shex = [int(line.strip(), 16) for line in s_txt.read_text().splitlines()]  # 10
    bhex = [int(line.strip(), 16) for line in b_txt.read_text().splitlines()]  # 10

    def int8_from_hex(v):
        v &= 0xFF
        return v-256 if v>=128 else v

    W = np.array([int8_from_hex(v) for v in whex], dtype=np.int32).reshape(10, 48)
    S = np.array([int8_from_hex(v) for v in shex], dtype=np.int32)   # alpha Q2.6
    B = np.array([int8_from_hex(v) for v in bhex], dtype=np.int32)   # bias  Q2.6

    Wt = np.clip(W, -1, 1)

    # [FIX] RTL FC 누적 정밀도(28-bit) 반영
    acc_max = (1 << (FC_ACCUM_BITS - 1)) - 1
    acc_min = -(1 << (FC_ACCUM_BITS - 1))
    psum_f32 = (Wt * feat[None,:]).sum(axis=1).astype(np.int64)
    psum = np.clip(psum_f32, acc_min, acc_max)

    alpha = S.astype(np.int64)
    bias  = B.astype(np.int64)

    # RTL 라운딩 (Round-half-up)
    scaled = (psum * alpha + ROUND_TERNARY) >> SHIFT_TERNARY
    bias_i = (bias + ROUND_TERNARY) >> SHIFT_TERNARY

    out = scaled + bias_i
    out = np.clip(out, SAT_MIN, SAT_MAX).astype(np.int32)

    Path("runs/debug_dump").mkdir(parents=True, exist_ok=True)
    Path("runs/debug_dump/fc_logits_txt.txt").write_text(
        "\n".join(str(int(v)) for v in out), encoding="utf-8"
    )
    print("[CHECK] txt기반 로짓(10) -> runs/debug_dump/fc_logits_txt.txt")

